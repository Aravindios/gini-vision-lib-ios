import UIKit


/**
 Provides a way to get all possible font weights used in the GiniVision library.
 
 **Possible weights:**
 
 * regular
 * bold
 * light
 * thin
 
 */

public struct GiniVisionFont {
    var regular: UIFont
    var bold: UIFont
    var light: UIFont
    var thin: UIFont
}
